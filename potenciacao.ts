let base: number = Number(prompt("Digite a base:"));
let expoente: number = Number(prompt("Digite o expoente:"));

function Potenciacao(base: number, expoente: number): number {

    for(let i = 0; i < expoente; i++) {

        return base ** expoente
        
    }
}

document.write("a potência é ", Potenciacao(base, expoente))